// MaintenancePipelineRun is the Schema for the maintenancepipelineruns API.
// It is auto-generated by the MaintenancePipeline controller — one instance per unique BMC
// covered by the pipeline's serverSelector. It owns all BMCSettings, BMCVersion, BIOSSettings,
// and BIOSVersion child resources for both the BMC and all servers that share it.
type MaintenancePipelineRun struct {
	metav1.TypeMeta   `json:",inline"`
	metav1.ObjectMeta `json:"metadata,omitempty"`

	Spec   MaintenancePipelineRunSpec   `json:"spec,omitempty"`
	Status MaintenancePipelineRunStatus `json:"status,omitempty"`
}

// +kubebuilder:object:root=true

// MaintenancePipelineRunList contains a list of MaintenancePipelineRun.
type MaintenancePipelineRunList struct {
	metav1.TypeMeta `json:",inline"`
	metav1.ListMeta `json:"metadata,omitempty"`
	Items           []MaintenancePipelineRun `json:"items"`
}

func init() {
	SchemeBuilder.Register(&MaintenancePipelineRun{}, &MaintenancePipelineRunList{})
}
